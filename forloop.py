#for loop used to iterate thro a sequence(list,tuple,dictionary)
#loop thro a list
fruits=["oranges","mangoes","avocado","pinneapples"]
for fruit in fruits:
    print(fruit)
    #loop through a string
for x in "python":
     print(x)
    #loop through a range range():
for y in range(7):
     print(y)
# loop through a range(start,stop)
for z in range(8,16):
     print(z)
     #step range(start,stop step)
for  a in range(20,30,2):
     print(a)